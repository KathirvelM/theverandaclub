package com.verandah.club.data.model.home;

public class Issue {
    int id;
    String number;
    String date;
    String volume_id;

    public int getId() {
        return id;
    }

    public String getNumber() {
        return number;
    }

    public String getDate() {
        return date;
    }

    public String getVolume_id() {
        return volume_id;
    }

}
